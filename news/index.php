          			<div id="l_t3">作者：英国《金融时报》&nbsp <font color="#086CC4">彼得•马什</font></div>
          			<div id="l_t4">字号 背景 </div><div id="l_t5">收藏 电邮 打印 评论<font color="#9E2F2F">[1条] 英文 对照</font></div>
